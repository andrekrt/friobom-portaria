function menuDescarga(){

    if(document.getElementById("submenuDescarga").style.display == "none" || document.getElementById("submenuDescarga").style.display == "" ){
        document.getElementById("submenuDescarga").style.display = "block";
    }else{
        document.getElementById("submenuDescarga").style.display = "none";
    }
    
}

